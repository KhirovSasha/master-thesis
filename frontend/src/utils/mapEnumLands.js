export default function mapLegalStatus(legalStatus) {
  const LegalStatusEnum = {
    0: "Земля сільськогосподарського призначення",
    1: "Земля населеного пункта",
    2: "Земля промислового та комерційного призначення",
    3: "Земля рекреаційного призначення",
    4: "Земля для будівництва",
    5: "Земля водного фонду",
    6: "Земля лісового фонду",
    7: "Земля об'єктів та спеціального призначення",
  };

  return LegalStatusEnum[legalStatus] || "Unknown Legal Status";
}
